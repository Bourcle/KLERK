import argparse
import json
from datetime import datetime, timezone
from pathlib import Path
from typing import Any

from eval.dataset import load_jsonl
from eval.metrics import compute_all_metrics
from src.utils.config import get_settings


def avg(metrics: dict[str, Any], name: str) -> float | None:
    """Extract the average score for a named metric.

    Args:
        metrics: Metric result dictionary keyed by metric name.
        name: Metric name whose average value should be extracted.

    Returns:
        float | None: Average metric score, or None when unavailable.
    """

    value = metrics.get(name, {}).get("average")
    return float(value) if value is not None else None


def diff(klerk: float | None, baseline: float | None) -> dict[str, Any]:
    """Calculate absolute and relative metric differences between KLERK and baseline scores.

    Args:
        klerk: KLERK metric score.
        baseline: Baseline metric score.

    Returns:
        dict[str, Any]: Difference result containing absolute and relative percentage changes.
    """

    if klerk is None or baseline is None:
        return {"absolute": None, "relative_percent": None}
    absolute = klerk - baseline
    relative = None if baseline == 0 else (absolute / abs(baseline)) * 100.0
    return {"absolute": absolute, "relative_percent": relative}


def summarize(
    klerk_rows: list[dict[str, Any]], baseline_rows: list[dict[str, Any]], *, bleurt_checkpoint: str | None
) -> dict[str, Any]:
    """Build an evaluation summary comparing KLERK results against MCP-only baseline results.

    Args:
        klerk_rows: Evaluation rows generated by the KLERK pipeline.
        baseline_rows: Evaluation rows generated by the MCP-only baseline.
        bleurt_checkpoint: Optional BLEURT checkpoint path used for BLEURT evaluation.

    Returns:
        dict[str, Any]: Evaluation summary containing metrics, configuration, differences, and per-question metadata.
    """

    settings = get_settings()
    klerk_metrics = compute_all_metrics(klerk_rows, bleurt_checkpoint=bleurt_checkpoint)
    baseline_metrics = compute_all_metrics(baseline_rows, bleurt_checkpoint=bleurt_checkpoint)
    recovery_used = sum(1 for row in klerk_rows if row.get("recovery_steps"))
    mcp_used = sum(1 for row in klerk_rows if row.get("used_mcp"))

    return {
        "generated_at": datetime.now(timezone.utc).isoformat(),
        "dataset_size": len(klerk_rows),
        "model_config": {
            "provider": settings.local_llm_provider,
            "model": settings.local_llm_model,
            "base_url": settings.local_llm_base_url,
        },
        "embedding_config": {
            "provider": settings.local_embedding_provider,
            "model": settings.local_embedding_model,
        },
        "klerk_metrics": klerk_metrics,
        "mcp_only_metrics": baseline_metrics,
        "differences": {
            "bertscore": diff(avg(klerk_metrics, "bertscore"), avg(baseline_metrics, "bertscore")),
            "bleurt": diff(avg(klerk_metrics, "bleurt"), avg(baseline_metrics, "bleurt")),
        },
        "recovery_loop_used_count": recovery_used,
        "mcp_augmentation_used_count": mcp_used,
        "per_question": [
            {
                "id": row.get("id"),
                "selected_collection": row.get("selected_collection"),
                "retrieval_sufficient": row.get("retrieval_sufficient"),
                "used_mcp": row.get("used_mcp"),
                "recovery_steps": len(row.get("recovery_steps", [])),
                "citations_valid": row.get("citation_validation", {}).get("valid"),
            }
            for row in klerk_rows
        ],
    }


def write_report(summary: dict[str, Any], output_dir: str | Path) -> None:
    """Write evaluation summary artifacts as JSON and Markdown reports.

    Args:
        summary: Evaluation summary generated by summarize.
        output_dir: Directory where report files should be written.

    Returns:
        None: This function completes after writing evaluation artifacts.
    """

    out = Path(output_dir)
    out.mkdir(parents=True, exist_ok=True)
    (out / "evaluation_summary.json").write_text(
        json.dumps(summary, ensure_ascii=False, indent=2),
        encoding="utf-8",
    )

    lines = [
        "# KLERK Evaluation Report",
        "",
        f"- Generated at: {summary['generated_at']}",
        f"- Dataset size: {summary['dataset_size']}",
        f"- LLM: {summary['model_config']['provider']} / {summary['model_config']['model']}",
        f"- Embedding: {summary['embedding_config']['provider']} / {summary['embedding_config']['model']}",
        f"- Recovery loop used: {summary['recovery_loop_used_count']}",
        f"- MCP augmentation used: {summary['mcp_augmentation_used_count']}",
        "",
        "## Metrics",
        "",
        "| Metric | KLERK | MCP-only | Absolute Diff | Relative Improvement | Notes |",
        "|---|---:|---:|---:|---:|---|",
    ]

    for key, label in [("bertscore", "BERTScore F1"), ("bleurt", "BLEURT")]:
        k_metric = summary["klerk_metrics"][key]
        b_metric = summary["mcp_only_metrics"][key]
        diff = summary["differences"][key]
        note = ""
        if not k_metric.get("available"):
            note = k_metric.get("reason", "")
        elif not b_metric.get("available"):
            note = b_metric.get("reason", "")
        lines.append(
            "| {label} | {k} | {b} | {d} | {r} | {note} |".format(
                label=label,
                k=f"{k_metric.get('average'):.4f}" if k_metric.get("average") is not None else "N/A",
                b=f"{b_metric.get('average'):.4f}" if b_metric.get("average") is not None else "N/A",
                d=f"{diff.get('absolute'):.4f}" if diff.get("absolute") is not None else "N/A",
                r=f"{diff.get('relative_percent'):.2f}%" if diff.get("relative_percent") is not None else "N/A",
                note=note or "",
            )
        )

    lines.extend(
        [
            "",
            "## Per Question Summary",
            "",
            "| ID | Collection | Sufficient | MCP | Recovery Steps | Citations Valid |",
            "|---|---|---:|---:|---:|---:|",
        ]
    )
    for row in summary["per_question"]:
        lines.append(
            f"| {row['id']} | {row['selected_collection']} | {row['retrieval_sufficient']} | "
            f"{row['used_mcp']} | {row['recovery_steps']} | {row['citations_valid']} |"
        )

    lines.extend(
        [
            "",
            "## Resume Safety",
            "",
            "Do not use sample dataset results as resume metrics. Use only results generated from the full 50-question dataset and keep both evaluation_summary.json and this report as artifacts.",
        ]
    )
    (out / "evaluation_report.md").write_text("\n".join(lines), encoding="utf-8")


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="Generate KLERK evaluation report.")
    parser.add_argument("--klerk", required=True)
    parser.add_argument("--baseline", required=True)
    parser.add_argument("--output-dir", required=True)
    parser.add_argument("--bleurt-checkpoint", default=None)
    return parser.parse_args()


if __name__ == "__main__":
    args = parse_args()
    summary = summarize(
        load_jsonl(args.klerk),
        load_jsonl(args.baseline),
        bleurt_checkpoint=args.bleurt_checkpoint,
    )
    write_report(summary, args.output_dir)
